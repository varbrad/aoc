import { readFile } from '../../utils/readFile'
import { part2 } from './solution'

describe('2019 - Day 10 - Part 2', () => {
  it('should solve a simple puzzle #1', () => {
    expect(
      part2(
        `.#....#####...#..
        ##...##.#####..##
        ##...#...#.#####.
        ..#.....#...###..
        ..#.#.....#....##`,
        { x: 8, y: 3 },
        36,
      ),
    ).toEqual(1303) // { x: 13, y: 3 }
  })
  it('should solve a simple puzzle', () => {
    expect(
      part2(
        `
      .#..##.###...#######
      ##.############..##.
      .#.######.########.#
      .###.#######.####.#.
      #####.##.#.##.###.##
      ..#####..#.#########
      ####################
      #.####....###.#.#.##
      ##.#################
      #####.##.###..####..
      ..######..##.#######
      ####.##.####...##..#
      .#####..#.######.###
      ##...#.##########...
      #.##########.#######
      .####.#.###.###.#.##
      ....##.##.###..#####
      .#.#.###########.###
      #.#.#.#####.####.###
      ###.##.####.##.#..##
    `,
        { x: 11, y: 13 },
      ),
    ).toEqual(802)
  })

  it('should solve our puzzle input', () => {
    const input = readFile(__dirname, 'input.txt')
    expect(part2(input, { x: 27, y: 19 })).toMatchInlineSnapshot(`1513`)
  })
})
